MapDrawer::MapDrawer(Map* pMap, const string &strSettingPath):mpMap(pMap)
{
    cv::FileStorage fSettings(strSettingPath, cv::FileStorage::READ);

    mKeyFrameSize = fSettings["Viewer.KeyFrameSize"];
    mKeyFrameLineWidth = fSettings["Viewer.KeyFrameLineWidth"];
    mGraphLineWidth = fSettings["Viewer.GraphLineWidth"];
    mPointSize = fSettings["Viewer.PointSize"];
    mCameraSize = fSettings["Viewer.CameraSize"];
    mCameraLineWidth = fSettings["Viewer.CameraLineWidth"];

}

void MapDrawer::DrawMapPoints()
{
    const vector<MapPoint*> &vpMPs = mpMap->GetAllMapPoints();
    const vector<MapPoint*> &vpRefMPs = mpMap->GetReferenceMapPoints();
    const vector<MapPoint*> &vpDsoMPsBeforeSim3Alignment = mpMap->GetAllDsoMapPointsBeforeSim3Alignment();
    const vector<MapPoint*> &vpDsoMPsAfterSim3Alignment = mpMap->GetAllDsoMapPointsAfterSim3Alignment();

    set<MapPoint*> spRefMPs(vpRefMPs.begin(), vpRefMPs.end());

    if(vpMPs.empty())
        return;

    glPointSize(mPointSize);
    glBegin(GL_POINTS);
    glColor3f(0.0,0.0,0.0);

    for(size_t i=0, iend=vpMPs.size(); i<iend;i++)
    {
        if(vpMPs[i]->isBad() || spRefMPs.count(vpMPs[i]))
            continue;
        cv::Mat pos = vpMPs[i]->GetWorldPos();
        glVertex3f(pos.at<float>(0),pos.at<float>(1),pos.at<float>(2));
    }
    glEnd();

    glPointSize(mPointSize);
    glBegin(GL_POINTS);
    glColor3f(1.0,0.0,0.0);

    for(set<MapPoint*>::iterator sit=spRefMPs.begin(), send=spRefMPs.end(); sit!=send; sit++)
    {
        if((*sit)->isBad())
            continue;
        cv::Mat pos = (*sit)->GetWorldPos();
        glVertex3f(pos.at<float>(0),pos.at<float>(1),pos.at<float>(2));

    }

    glEnd();

    // Seong Addition: Visualize DSO points
    if (!vpDsoMPsBeforeSim3Alignment.empty())
    {
        glPointSize(mPointSize);
        glBegin(GL_POINTS);
        glColor3f(0.0,0.0,1.0);

        for(size_t i=0, iend=vpDsoMPsBeforeSim3Alignment.size(); i<iend;i++)
        {
            if (vpDsoMPsBeforeSim3Alignment[i]->GetReferenceKeyFrame()->isBad())
                continue;

            cv::Mat Twc = vpDsoMPsBeforeSim3Alignment[i]->GetReferenceKeyFrame()->GetPoseInverse();
            cv::Mat pos = Twc.rowRange(0,3).colRange(0,3)* vpDsoMPsBeforeSim3Alignment[i]->GetWorldPos() + Twc.rowRange(0,3).col(3);
            // Note: vpDsoMPs[i]->GetWorldPos() is actually in camera frame!

            glVertex3f(pos.at<float>(0),pos.at<float>(1),pos.at<float>(2));
        }
        glEnd();
    }
    else if (!vpDsoMPsAfterSim3Alignment.empty())
    {
        glPointSize(mPointSize);
        glBegin(GL_POINTS);
        glColor3f(0.0,0.0,1.0);

        for(size_t i=0, iend=vpDsoMPsAfterSim3Alignment.size(); i<iend;i++)
        {
            if (vpDsoMPsAfterSim3Alignment[i]->GetReferenceKeyFrame()->isBad())
                continue;

            cv::Mat pos = vpDsoMPsAfterSim3Alignment[i]->GetWorldPos();

            glVertex3f(pos.at<float>(0),pos.at<float>(1),pos.at<float>(2));
        }
        glEnd();
    }
}

void MapDrawer::DrawKeyFrames(const bool bDrawKF, const bool bDrawGraph)
{
    const float &w = mKeyFrameSize;
    const float h = w*0.75;
    const float z = w*0.6;

    const vector<KeyFrame*> vpKFs = mpMap->GetAllKeyFrames();
    const vector<cv::Mat> Twc_dso_kfs = mpMap->GetDsoKFsPoses();

    cv::Mat Twc_dso = cv::Mat::eye(4,4,CV_32F);


    if(bDrawKF)
    {
        for(size_t i=0; i<vpKFs.size(); i++)
        {
            KeyFrame* pKF = vpKFs[i];
            //cv::Mat Twc = pKF->GetPoseInverse().t();
            cv::Mat Twc_final = pKF->GetFinalPoseInverse();

            if (cv::sum(Twc_final.rowRange(0,3).col(3))[0]==0)
                continue;

            cv::Mat Twc = Twc_final.t();

            glPushMatrix();

            glMultMatrixf(Twc.ptr<GLfloat>(0));

            glLineWidth(mKeyFrameLineWidth*2);
            glColor3f(0.1f,0.5f,0.1f);
//            if (pKF->hasNoncollinearCovlinks())
//                glColor3f(0.1f,0.5f,0.1f);
//            else
//                glColor3f(1.0f,0.0f,0.0f); //glColor3f(0.1f,0.5f,0.1f);

            glBegin(GL_LINES);
            glVertex3f(0,0,0);
            glVertex3f(w,h,z);
            glVertex3f(0,0,0);
            glVertex3f(w,-h,z);
            glVertex3f(0,0,0);
            glVertex3f(-w,-h,z);
            glVertex3f(0,0,0);
            glVertex3f(-w,h,z);

            glVertex3f(w,h,z);
            glVertex3f(w,-h,z);

            glVertex3f(-w,h,z);
            glVertex3f(-w,-h,z);

            glVertex3f(-w,h,z);
            glVertex3f(w,h,z);

            glVertex3f(-w,-h,z);
            glVertex3f(w,-h,z);
            glEnd();

            glPopMatrix();
        }

        // DSO KFs
        for(size_t i=0; i<Twc_dso_kfs.size(); i++)
        {
            cv::Mat Twc = Twc_dso_kfs[i].clone().t();

            glPushMatrix();

            glMultMatrixf(Twc.ptr<GLfloat>(0));

            glLineWidth(mKeyFrameLineWidth*2);
            glColor3f(0.0f,0.0f,1.0f);
            glBegin(GL_LINES);
            glVertex3f(0,0,0);
            glVertex3f(w,h,z);
            glVertex3f(0,0,0);
            glVertex3f(w,-h,z);
            glVertex3f(0,0,0);
            glVertex3f(-w,-h,z);
            glVertex3f(0,0,0);
            glVertex3f(-w,h,z);

            glVertex3f(w,h,z);
            glVertex3f(w,-h,z);

            glVertex3f(-w,h,z);
            glVertex3f(-w,-h,z);

            glVertex3f(-w,h,z);
            glVertex3f(w,h,z);

            glVertex3f(-w,-h,z);
            glVertex3f(w,-h,z);
            glEnd();

            glPopMatrix();
        }
    }

    if(bDrawGraph)
    {
        glLineWidth(mGraphLineWidth);
        glColor4f(0.0f,1.0f,0.0f,0.6f);
        glBegin(GL_LINES);

        for(size_t i=0; i<vpKFs.size(); i++)
        {
            // Covisibility Graph
            const vector<KeyFrame*> vCovKFs = vpKFs[i]->GetCovisiblesByWeight(100);
            //cv::Mat Ow = vpKFs[i]->GetCameraCenter();
            cv::Mat Ow = vpKFs[i]->GetFinalCameraCenter();
            if (cv::sum(Ow)[0]==0) continue;

            if(!vCovKFs.empty())
            {
                for(vector<KeyFrame*>::const_iterator vit=vCovKFs.begin(), vend=vCovKFs.end(); vit!=vend; vit++)
                {
                    if((*vit)->mnId<vpKFs[i]->mnId)
                        continue;
                    //cv::Mat Ow2 = (*vit)->GetCameraCenter();
                    cv::Mat Ow2 = (*vit)->GetFinalCameraCenter();
                    if (cv::sum(Ow2)[0]==0) continue;

                    glVertex3f(Ow.at<float>(0),Ow.at<float>(1),Ow.at<float>(2));
                    glVertex3f(Ow2.at<float>(0),Ow2.at<float>(1),Ow2.at<float>(2));
                }
            }

            // Spanning tree
            KeyFrame* pParent = vpKFs[i]->GetParent();
            if(pParent)
            {
                //cv::Mat Owp = pParent->GetCameraCenter();
                cv::Mat Owp = pParent->GetFinalCameraCenter();
                if (cv::sum(Owp)[0]==0) continue;

                glVertex3f(Ow.at<float>(0),Ow.at<float>(1),Ow.at<float>(2));
                glVertex3f(Owp.at<float>(0),Owp.at<float>(1),Owp.at<float>(2));
            }

            // Loops
            set<KeyFrame*> sLoopKFs = vpKFs[i]->GetLoopEdges();
            for(set<KeyFrame*>::iterator sit=sLoopKFs.begin(), send=sLoopKFs.end(); sit!=send; sit++)
            {
                if((*sit)->mnId<vpKFs[i]->mnId)
                    continue;
                //cv::Mat Owl = (*sit)->GetCameraCenter();
                cv::Mat Owl = (*sit)->GetFinalCameraCenter();
                if (cv::sum(Owl)[0]==0) continue;

                glVertex3f(Ow.at<float>(0),Ow.at<float>(1),Ow.at<float>(2));
                glVertex3f(Owl.at<float>(0),Owl.at<float>(1),Owl.at<float>(2));
            }
        }

        glEnd();
    }
}

